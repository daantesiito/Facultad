package practica2.tp2.ejercicio1;

public class DosEnt {
    private int sum;
    private int dif;
    
    public DosEnt(int sum, int dif) {
        this.sum = sum;
        this.dif = dif;
    }
    
    public int getSum() {
        return sum;
    }
    
    public int getDif() {
        return dif;
    }
}
